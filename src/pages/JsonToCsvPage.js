import React, { useState } from "react";
import { jsonToCsv } from "../utils/jsonToCsv";
import Header from "../components/Header";

function JsonToCsvPage() {
  const [jsonInput, setJsonInput] = useState("");
  const [csvOutput, setCsvOutput] = useState("");

  const handleConvert = () => {
    try {
      const result = jsonToCsv(jsonInput);
      setCsvOutput(result);
    } catch (err) {
      alert(err.message);
    }
  };

  const handleCopy = () => {
    navigator.clipboard.writeText(csvOutput);
    alert("CSV copied!");
  };

  const handleDownload = () => {
    const blob = new Blob([csvOutput], { type: "text/csv" });
    const link = document.createElement("a");
    link.href = URL.createObjectURL(blob);
    link.download = "result.csv";
    link.click();
  };

  const handleFileUpload = (e) => {
    const file = e.target.files[0];
    if (!file) return;

    const reader = new FileReader();
    reader.onload = () => {
      setJsonInput(reader.result);
      try {
        const result = jsonToCsv(reader.result);
        setCsvOutput(result);
      } catch (err) {
        alert("Error converting JSON.");
      }
    };
    reader.readAsText(file);
  };

  return (
    <div>
      <Header />
    <div className="container py-4">
      <h2 className="mb-4">JSON to CSV</h2>

      <div className="mb-3">
        <label className="form-label">Upload JSON file:</label>
        <input type="file" className="form-control" accept=".json" onChange={handleFileUpload} />
      </div>

      <textarea
        className="form-control mb-3"
        rows="6"
        placeholder="Input JSON..."
        value={jsonInput}
        onChange={(e) => setJsonInput(e.target.value)}
      />

      <button className="btn btn-warning me-2" onClick={handleConvert}>Convert</button>

      {csvOutput && (
        <>
          <h5 className="mt-4">CSV result:</h5>
          <pre className="bg-light p-3 border rounded" style={{ maxHeight: "300px", overflow: "auto" }}>
            {csvOutput}
          </pre>
          <button type="button" class="btn btn-outline-warning me-2" onClick={handleCopy}>
              <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="currentColor" class="bi bi-copy me-2" viewBox="0 0 16 16">
                <path fill-rule="evenodd" d="M4 2a2 2 0 0 1 2-2h8a2 2 0 0 1 2 2v8a2 2 0 0 1-2 2H6a2 2 0 0 1-2-2zm2-1a1 1 0 0 0-1 1v8a1 1 0 0 0 1 1h8a1 1 0 0 0 1-1V2a1 1 0 0 0-1-1zM2 5a1 1 0 0 0-1 1v8a1 1 0 0 0 1 1h8a1 1 0 0 0 1-1v-1h1v1a2 2 0 0 1-2 2H2a2 2 0 0 1-2-2V6a2 2 0 0 1 2-2h1v1z"></path>
              </svg>
              Copy
          </button>
          <button type="button" class="btn btn-secondary" onClick={handleDownload}>
            <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" fill="currentColor" class="bi bi-filetype-csv me-2" viewBox="0 0 16 16">
              <path fill-rule="evenodd" d="M14 4.5V14a2 2 0 0 1-2 2h-1v-1h1a1 1 0 0 0 1-1V4.5h-2A1.5 1.5 0 0 1 9.5 3V1H4a1 1 0 0 0-1 1v9H2V2a2 2 0 0 1 2-2h5.5zM3.517 14.841a1.13 1.13 0 0 0 .401.823q.195.162.478.252.284.091.665.091.507 0 .859-.158.354-.158.539-.44.187-.284.187-.656 0-.336-.134-.56a1 1 0 0 0-.375-.357 2 2 0 0 0-.566-.21l-.621-.144a1 1 0 0 1-.404-.176.37.37 0 0 1-.144-.299q0-.234.185-.384.188-.152.512-.152.214 0 .37.068a.6.6 0 0 1 .246.181.56.56 0 0 1 .12.258h.75a1.1 1.1 0 0 0-.2-.566 1.2 1.2 0 0 0-.5-.41 1.8 1.8 0 0 0-.78-.152q-.439 0-.776.15-.337.149-.527.421-.19.273-.19.639 0 .302.122.524.124.223.352.367.228.143.539.213l.618.144q.31.073.463.193a.39.39 0 0 1 .152.326.5.5 0 0 1-.085.29.56.56 0 0 1-.255.193q-.167.07-.413.07-.175 0-.32-.04a.8.8 0 0 1-.248-.115.58.58 0 0 1-.255-.384zM.806 13.693q0-.373.102-.633a.87.87 0 0 1 .302-.399.8.8 0 0 1 .475-.137q.225 0 .398.097a.7.7 0 0 1 .272.26.85.85 0 0 1 .12.381h.765v-.072a1.33 1.33 0 0 0-.466-.964 1.4 1.4 0 0 0-.489-.272 1.8 1.8 0 0 0-.606-.097q-.534 0-.911.223-.375.222-.572.632-.195.41-.196.979v.498q0 .568.193.976.197.407.572.626.375.217.914.217.439 0 .785-.164t.55-.454a1.27 1.27 0 0 0 .226-.674v-.076h-.764a.8.8 0 0 1-.118.363.7.7 0 0 1-.272.25.9.9 0 0 1-.401.087.85.85 0 0 1-.478-.132.83.83 0 0 1-.299-.392 1.7 1.7 0 0 1-.102-.627zm8.239 2.238h-.953l-1.338-3.999h.917l.896 3.138h.038l.888-3.138h.879z"></path>
            </svg>
            Download CSV
          </button>
        </>
      )}
    </div>
    </div>
  );
}

export default JsonToCsvPage;

